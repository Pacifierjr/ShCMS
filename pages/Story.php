<h3>Chapter 1: In the Beginning</h3>

<div class="image image-left" style="width:191px"><img src="img/Story/Story1_0.jpg"></div>

<p>In a time before time, when there was only darkness, the Goddess Etain came to this universe with a heart bearing desire for order and harmony. Immersing herself in a vast ocean of her own creation, she formed the continents of Teos Epeiros from her living flesh. Mountains were made high and rivers ran deep throughout the land in likeness of the veins that ran within her body. She created green forests and plains that engulfed the lands in the likeness of her youthful complexion. The sky and stars inherited their exquisiteness from the likeness of her eyes. This vast, rich world crafted through her strength, will, and desire for perfection, was named Shaiya.</p>

<p>All lives began from the hands of the Goddess of Etain. At first, she created the Thelaiosis, Dragon Gods who could help her rule the kingdom of Teos Epeiros. Threemans, leader amongst the Dragons, put forth that further races would aid the growth of the young world. It was with his help that the Nordein were born.</p>

<h3>Chapter 2: Rebirth</h3>

<p>Tragically, the first Children of Etain were deemed failures in her eyes. Though strong of body and mind, the Nordein possessed brutal, aggressive forms and were cast back into the soil to be forgotten. Determined to make her next creations with a clear vision, the Dumianas were sculpted in Her own likeness and imbued with the virtues of the Thelaiosis. At last, Teos was complete with life and all prospered in harmony, worshipping the Goddess.</p>

<p>But in time, the fledgling world saw troubles the Goddess had never expected. She had given the Dumianas the gift of intelligence and knowledge, not knowing that such blessings would eventually foster ambitions, doubts, and growing disrespect and denial of her authority. The Dumianas rebelled and started a war amongst themselves that lasted for countless years. As the fighting waged on, the Goddess Etaine became depressed with her failed creation and weakened by her link to the bloodied world. Her beauty faded along with her power.</p>

<h3>Chapter 3: Betrayal</h3>

<div class="image image-right" style="width:245px"><img src="img/Story/Story3_0.jpg"></div>

<p>In an act of shocking arrogance and blasphemy, the Dumianas ended the vulnerable Goddess’ life with the stroke of a common sword. Upon her death, her tormented soul was torn into two halves. The land shook and the sky darkened as the living things throughout the world of Shaiya were devoured into the soil, as if to hide the sins they had committed.</p>

<p>Nearly all of Teos Epeiros' inhabitants were destroyed. Fate allowed two remaining Dumianas, Proton and Eustatin, to survive. Desiring to keep their line alive, Eustatin approached Proton as he slept. But Proton, once awake, rejected Eustatin and banished her in rash anger to the south of Teos.</p>

<h3>Chapter 4: The Races of Teos</h3>

<div class="image image-left" style="width:203px"><img src="img/Story/Story2_0.jpg"></div>

<p>In solitude, Eustatin bore their son, raising him in darkness, filling him with the fury and anguish she held against Proton. When she died, a young woman stepped forth from her cremation ashes to share a life with her son, now grown into adulthood. They became the ancestors of the Vail race.</p>

<p>For Proton, his life remained haunted by the actions taken by the Dumianas toward the Goddess Etain. In his loneliness, he created a statue in the image of her in a tree. One day, the wooden figurine turned into a living woman infused with the blood of the Earth, and together with Proton founded the Elven race.</p>

<p>The two races of Vail and Elves lived apart for hundreds of years, but as their realms expanded and they came into contact once more, the hatred and resentment of the past rose once again to the fore. The two races pushed into each others’ lands in a constant struggle with untold lives lost on both sides, but were ultimately stalemated.</p>

<h3>Chapter 5: The Goddesses Emerge</h3>

<p>As for the Goddess Etain, her body vanished with her death, and her spirit remained divided and dormant. But as the ages passed, new energies filled the remnants of her split soul, gathering in power, until one night marked by an eclipse, the world shook in creation once more.</p>

<p>The ground cracked wildly and monstrous storms wracked the skies. The Vail and Elves put a hold to their war in fear, for they now beheld two Goddesses never seen before. One of Light and one of Darkness.</p>

<h3>Chapter 6: Rise of Light and Darkness</h3>

<div class="image image-left" style="width:191px"><img src="img/Story/Story4_0.jpg"></div>

<p>A second preternatural eclipse marked the entry of new life into Shaiya. From the oceans came the Humans, seeking new lands for their fast-growing race. The Humans forged a realm for themselves in their home of Teos, and found eager allies in the Elves under the watchful and benevolent eyes of the Goddess of Light. Thus the Alliance of Light was born.</p>

<p>In that same eclipse, in the southern lands of the Vail, the ground cracked open and the Nordein that were once cast away emerged from the underworld with an evolved, stronger form- the Deatheaters. Arrogant, aggressive, and reckless at first, they waged war on all the races of Teos as vengeance for their confinement. When they found themselves on the verge of defeat at the hands of the Human-Elf Alliance, the Deatheaters were rescued by the Vail, who aspired to create a unified force against the Alliance of Light. Together the Vail and the Deatheaters founded the Union of Fury and worshipped the seductive and powerful Goddess of Darkness.</p>



