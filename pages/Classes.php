<h2>Human Classes</h2>

<p>Classes in Shaiya are specific to character race.</p>

<h3>Defender</h3>

<div class="image image-right"><img src="img/story/human_defender.png"></div>

<p>The Defender is a hard-as-nails tank. Defenders have high stamina and high hit points, enabling them to take a beating while dishing one out. Unlike Fighters, Defenders can take on multiple foes at the same time with little worry. Defenders also have the ability to provoke enemies into attacking them, a useful skill when adventuring in groups.</p>
<em>Characteristics:</em>

<ul>
	<li>Uses shields, one-handed swords, and blunt weapons</li>
	<li>High vitality and stamina</li>
	<li>First into the fray</li>
	<li>Automatically gains Reaction Defense boost and HP (+1 to REC) each level</li>
</ul>

<div class="clear">&nbsp;</div>

<h3>Fighter</h3>

<div class="image image-right"><img src="img/story/human_fighter.png"></div>

<p>The Fighter is a fierce melee combatant, confronting foes up close and personal. Physical attack power is the focus of the Fighter, but don’t be fooled - a certain amount of magical points (MP) is needed to power the Fighter’s devastating special skills.</p>
<em>Characteristics:</em>

<ul>
	<li>Wide range of available weapons</li>
	<li>Powerful physical attacks</li>
	<li>Front-line brawler</li>
	<li>Automatically gains +1 to Strength (STR) each level</li>
</ul>

<div class="clear">&nbsp;</div>

<h3>Priest</h3>

<div class="image image-right"><img src="img/story/human_priest.png"></div>

<p>Relying on the spiritual nature of humans, the Priest is a true healer. The Priest uses healing magic to keep him/herself and party members healthy during battle. Priests can resurrect the dead, an extremely useful skill for keeping fallen comrades around in a tense situation. Priests also have a high resistance to magical attacks, making them a threat to Pagans.</p>
<em>Characteristics:</em>

<ul>
	<li>Curing powers</li>
	<li>Ability to transform allies into powerful monsters</li>
	<li>Ability to transform enemies into harmless animals</li>
	<li>Automatically gains + 1 Wisdom each level</li>
</ul>
</div>

<div class="tab-panel" id="Elves">
<h2>Elf Classes</h2>

<p>Classes in Shaiya are specific to character race.</p>

<h3>Archer</h3>

<div class="image image-right"><img src="img/story/elf_archer.png"></div>

<p>Striking from a distance helps keep Archers out of harm’s way. Archers use ranged attacks to wound and slow the enemy, as well as lower its attack power, or stop it in its tracks. Special skills of the Archer allow them to literally rain arrows down upon their targets. Archers also have Lady Luck smiling down on them, enabling them to make brutal critical hits.</p>
<em>Characteristics:</em>

<ul>
	<li>Uses bow, crossbow, javelin, and short swords</li>
	<li>Distanced attacks</li>
	<li>High accuracy</li>
	<li>Automatically gains fortune (+1 LUC) per level</li>
</ul>

<div class="clear">&nbsp;</div>

<h3>Mage</h3>

<div class="image image-right"><img src="img/story/elf_mage.png"></div>

<p>One of the hallmarks of the Elf race is their use of magic. No other class so exemplifies this than the Mage. Attacking up close or far away, targeting one foe or many, the Mage uses the power of magic to destroy their enemies. Their ability to harness the power of fire, water, earth, and wind makes the Mage a force to be reckoned with.</p>
<em>Characteristics:</em>

<ul>
	<li>Awe-inspiring attacks</li>
	<li>Single-target and area-of-effect attacks</li>
	<li>Automatically gains intelligence (+1 INT) per level</li>
</ul>

<div class="clear">&nbsp;</div>

<h3>Ranger</h3>

<div class="image image-right"><img src="img/story/elf_ranger.png"></div>

<p>The Ranger uses knowledge of the forest and the wilds to blend into his/her surroundings. Stealth and deception are trademarks of a good Ranger, while ambush and evasion are their staples in combat. They can also frighten beasts or disguise themselves as harmless creatures, further enabling their stealth.</p>
<em>Characteristics:</em>

<ul>
	<li>Uses short blades and claw weapons</li>
	<li>High evasion ability</li>
	<li>Stealth and disguise</li>
	<li>Automatically gains +1 DEX per level</li>
</ul>
</div>

