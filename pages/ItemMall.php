<h2>Purchasing from Shaiya’s Web based Item Mall</h2>

<p>Shaiya’s Web based Item Mall works differently from the Item Mall available within the game. Instead of being directly integrated with the game, you may also purchase items through the use of our website. To access the Shaiya web based item mall, click on the "Item Mall" link in the section link bar on the Shaiya website, click the "Game Items" feature graphic on the main page, or just <a href="/Store">go here</a>.</p>
<img src="img/story/ItemMall01.jpg">
<p>If you look at the top of the picture above (<strong>#1</strong>), you will see a <b>menu bar</b> containing the category tabs our items are divided into. By clicking them you will be directed to:</p>

<ul>
	<li><strong>Featured Items</strong>: Shows featured items from all categories - this section displays new items, hot sellers, and special sales limited in time or quantity!</li>
	<li><strong>Consumables</strong>: The consumables section includes all items that are are one-use only. They'll either disappear immediately upon use, or expire after a certain amount of time. Consumables are often sold in stack multiples to bring you a better deal. The consumables are generally restricted to potions and items that increase player stats temporarily.</li>
	<li><strong>Misc.</strong>: Items not covered by other categories appear in the Miscellaneous section, but these are some of the coolest items: bags that increase your inventory space, Reset Stats and Reset Skills, and teleportation items can all be found here.</li>
	<li><strong>Mystery Boxes</strong>: If you like getting some of the best items available through the item mall, this is the spot for you. Mystery Boxes reward a random item within a selection of items. You're going to have to take a gamble if you want the items here. 1 Random Item will be placed in the Bank.</li>
	<li><strong>Packages</strong>: The great thing about packages is that you get the most bang for your buck. These will give you a bunch of different items in one conveniently prepared package. Packages generally provide a discount for the items included and provides some great value.</li>
	<li><strong>Upgrade</strong>: Want to take your armor and weapons beyond their natural abilities? Use Linking Hammers to slot your armor so you can insert some powerful lapis.</li>
	<li><strong>Checkout</strong>: Got everything you need for now? Hit the checkout tab to go directly to the screen where you can purchase what's in your cart. You can choose which server to send your items to, and even which of your characters on a server you'd like to receive them.</li>
</ul>
&nbsp;

<p>The shopping menu (<strong>#2</strong>) shows all items within a category, along with each item's price in AP and a brief description of each item, along with any other information, such as whether an item can be traded or not. Hovering your pointer over or double-clicking an item will bring up a more detailed description of it. Each item also has a check box that you can click on to select. Once you have selected all the items that you want off of the current page, you can click on the "Add to Cart" button (<strong>#3</strong>). The page will also show you your current available Aeria Points balance (<strong>#4</strong>).</p>

<p><strong>Note:</strong> If you want to continue adding items from other categories, you MUST first add the items from the current page into your cart. Also, if you want more than one of an item, you must add each item one at a time.</p>

<p>Once you have clicked on the "Add to Cart" button, you will be taken to a page that looks similar to this:</p>
<img src="img/story/itemmall2kd5.jpg">
<p>The page will contain a table showing all the items currently in your shopping cart. From here, you have several choices:</p>

<ol>
	<li>Click the "continue shopping" button or any one of the other categories in the menu bar to access the various items page again. All the items already in your cart will remain saved, and you will be able to add more items or increase the quantity of an item already in your shopping cart.</li>
	<li>You can click on the trash can to remove specific items from the shopping cart.</li>
	<li>Click "Purchase" when you are ready to finalize your purchase. Please review your shopping cart carefully before purchasing.</li>
	<li>To completely remove all items from your shopping cart, click on the "Discard" button.</li>
</ol>
