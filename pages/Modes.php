<h2>Difficulty Mode Overview</h2>

<p>When creating a character, you need to select a name, class, sex,&nbsp;and a profession for your character. You will also have to choose the difficulty setting for your gameplay experience. The modes are as follows:</p>

<h3><span style="font-family: Arial, Helvetica; font-size: 19px; font-weight: bold; letter-spacing: -0.05em;">Basic Mode</span></h3>

<p>The Basic mode for any character in Shaiya.&nbsp;</p>

<ul>
	<li>Level cap is 80</li>
	<li>Able to use all items in Shaiya except Ultimate Mode Items</li>
	<li>7 stat points and 4 skill points per level</li>
</ul>

<p>&nbsp;</p>

<h3>Ultimate Mode</h3>

<p>The pinnacle of power is Ultimate Mode. This mode takes skill and patience.</p>

<p><strong>WARNING</strong>: Characters who die in Ultimate Mode will be deleted if not resurrected within 3 minutes. This mode is only for those players looking for the ultimate challenge.</p>

<ul>
	<li>More experience is required to level up in Ultimate Mode than Basic Mode</li>
	<li>Level cap is 80</li>
	<li>9 stat points and 5 skill points per level</li>
	<li>Able to use all items in the game</li>
	<li>Able to use some skills available only to Ultimate Mode characters</li>
	<li>If your character dies, you only have 3 minutes to be resurrected or your character is deleted</li>
</ul>
