<h2>Introduction</h2>

<p>Point, click, attack. Yes, combat can be that simple. However, you can also use your learned abilities and special skills to devastate your opponent. Once you have a learned a new ability, as long as it’s not a passive one, you can drag it over to your shortcut bar for quick use. (You can also put frequently used items in the shortcut bar.) As you progress in levels, you may find that one shortcut bar isn’t enough. We’ve got you covered. Click on the “+” on the shortcut bat to get a second one. You can also rotate the shortcut bar and move it wherever you want.</p>

<p>And don’t forget that a well-timed sidestep or back flip can sometimes get you out of harm’s way: double-tap Q or E to sidestep, and double-tap S to swiftly move backwards. These dodges won’t work against most magical attacks, but can be effective against physical ones.</p>

<h3>Monster ID System</h3>

<p>When deciding what monster to hunt, you can tell how much of a challenge they’ll be by clicking on them once to target them. The name of the monster will appear in one of the following colors, depending upon how difficult they are:</p>

<ul>
	<li><strong>White</strong> - 6 or more levels below you, no experience value</li>
	<li><strong>Light Blue</strong> - 4 or 5 levels below you, extremely low XP value</li>
	<li><strong>Blue</strong> - 2 or 3 levels below you, low XP value</li>
	<li><strong>Green</strong> - your level plus or minus one, decent XP value</li>
	<li><strong>Yellow</strong> - 2 or 3 levels above you, good XP value</li>
	<li><strong>Orange</strong> - 4 or 5 levels above you, great XP value but a tough fight</li>
	<li><strong>Red</strong> - 6 or 7 levels above you, great XP value but be prepared to die</li>
	<li><strong>Pink</strong> - 8 or 9 levels above you, phenomenal XP if you can bring it down</li>
	<li><strong>Grey</strong> - 10 or more levels above you, run away screaming (no XP value)</li>
</ul>

<h3>Player vs. Player (PvP) Combat</h3>

<div class="image image-right"><img src="img/story/pvp.jpg"></div>

<p>You can challenge members of your own realm to a duel by selecting them, right-clicking and choosing <em>Request Battle</em>. At low levels, a duel won’t help anyone much, except providing free entertainment for those around. No, it isn’t until the mid or higher levels that dueling really helps you to prepare to the real fights that are to come. And don’t just challenge players of your own class to see who's better; fight characters of other classes so you can develop counter-strategies against them.</p>

<p>When you finally do come face to face with a member of the opposing faction, you’ll notice two things: first, your map will begin to pulse red in anticipation of the imminent battle; and second, the enemy’s name shows up in red. No matter what your enemy’s levels are, they will always con red. When the fight commences, it will be very much like any other fight, except that you’ll be fighting a real person. And remember, most likely both of you will have skills that were specifically meant for a real opponent.</p>

<p style="text-align: center;"><img alt="" src="img/story/easter11.png" style="width: 100px; height: 100px;"></p>

<p>If you’re out picking herbs in the Borderlands and don’t necessarily want a fight, you can attempt to discourage the enemy… but best of luck with that. You’re from distant lands and do not speak the same language. There is a skill that will allow you to understand your foe, but it’s often simpler to just use Shayia’s emotes to express your feelings.</p>


<h3>Death Penalty</h3>

<ul>
	<li>When you die as an Easy, Normal or Hard mode character in Shaiya, you will lose some experience unless you have an item that prevents it.</li>
	<li>You also have a chance to lose equipped items or items in your character's inventory unless you have items to prevent that as well.</li>
	<li>As an Ultimate mode character, all of the previous applies, and if you are not resurrected within 3 minutes, your character is gone forever!</li>
</ul>

