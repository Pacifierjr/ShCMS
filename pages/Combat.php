<h2>How to gain ranks</h2>

<ol>
	<li>Enter a PvP battlefield. The battlefields are separated by level brackets. More details on PvP battlefields in Shaiya can be found <a href="http://shaiya.aeriagames.com/guide/linkedbattle">here.</a></li>
	<li>Defeat other players to gain kill points.</li>
</ol>

<h2>Advantages to ranking up</h2>

<h4>Stats</h4>
&nbsp;

<p>Ranking up allows players to gain access to more stats. These stats may be allocated just like other stats and are a bonus on top of stats gained while leveling up. Once a player reaches level 16, ranks can be redeemed for stat points in Apulune or Iris.</p>

<h4>Gold</h4>
&nbsp;

<p>Death is just another part of battle. Each death brings you one step closer to a small gold compensation. Only Hard mode and Ultimate mode characters qualify.</p>

<h4>Glory and fame</h4>
&nbsp;

<p>Glory on the battlefield and fame outside of it. Reach for the infamous red star and check out the <a href="http://shaiya.aeriagames.com/sygame/ranking/lailah" target="_blank">player rankings</a>. In-game ranks are denoted by a symbol next to the player's name and a title in the character sheet.</p>

