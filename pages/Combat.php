<h2>Battlegrounds</h2>

<p>When you're ready to take on the opposing faction, you can head over to the PVP Battlegrounds to fight for control of three different areas. Each Battleground is divided by levels so that players of roughly the same level are matched against each other.</p>

<p>The Proelium Frontier is where players of levels 1-15 can get started battling the enemy. You can access it from a portal in Gliter or ____. The objective of this battleground is to take and hold the altar in the middle of the map.</p>

<p>Cantabilian is the 20-30 map that you can access from portals in ___ or ___. The objective is to control all relics and kill all enemies.</p>

<p>D-Water is the ultimate PvP map for levels 1-60. To enter D-Water, go to the portal in ___ or ___.</p>

<h2>Linked Battlegrounds</h2>

<p>If you've destroyed everyone within reach on your server and want to prove yourself against new opponents, you can head to the Linked Battlegrounds. These are cross-server versions of the Battlegrounds that let players from all realms in Shaiya duke it out to see who reigns supreme.</p>

<h4>About</h4>
&nbsp;

<p>Each of the 3 battlefields are available.</p>

<ul>
	<li>Proelium Frontier (levels 10-15)</li>
	<li>Cantibilian (levels 20-30)</li>
	<li>D-Water Borderlands (levels 31-60)</li>
</ul>

<p>Players that are from different servers with the same name will have a number added to the end of their name.</p>

<h4>How to get to the linked battlefields</h4>
&nbsp;

<p>Enter your faction's capital city and find the preparation room.</p>

