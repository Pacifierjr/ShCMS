<p>The Goddess Blessing is one of the most unique characteristics of Shaiya. Each side of the war seeks the Blessing of their Goddess. Points for the Goddess Blessing are awarded when you slay a member of the opposing faction or when you destroy an enemy relic. The Goddess Blessing is bestowed not only upon you, but the entire realm. The blessing has a multitude of positive effects on you and your faction.</p>

<p style="text-align: center;"><img alt="" src="img/story/easter11.png" style="width: 100px; height: 100px;"></p>

<h3>Benefits of Goddess Blessing</h3>

<ol>
	<li>Increased success rate for upgrading weapons.</li>
	<li>Increase in attack power.</li>
	<li>Increase of maximum HP, MP, SP.</li>
	<li>Increase of HP, MP, SP restoration during rest.</li>
	<li>Increase of MP, SP power restoration during the major battle.</li>
	<li>Increase of Magic Attacking Power.</li>
	<li>Increase of Defending Power (physical, shooting, magic).</li>
	<li>Reduced experience value loss during resurrection.</li>
	<li>Reduced repair costs of damaged equipment.</li>
	<li>Reduce cool-down time when consuming items.</li>
</ol>
	
		
