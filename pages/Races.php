<h2>The Humans</h2>

<p>Newest to the lands of Teos are the Humans. Spiritual, strong, and swift, the Humans are adept at nearly every job that Teos has to offer. Their power, however, lies in their strength of body and spirit. Born Fighters and Priests, the Humans are a natural complement to the magical Elves. Brought to Teos by the Goddesses, the hope was that the Humans would help restore balance and unity. Unfortunately, the Humans were swept up in the chaos, and the Elves were the first to assist them.</p>
