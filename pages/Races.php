<div class="tab-panel" id="Elves">
<h2>The Elves</h2>

<p>Methodical, deliberate, and smart are just some of the words used to describe the Elves. The Elves, like the Vail, are descended from the Dumianas. The Laws of Nature are very important to the Elves, and their society relies on order and discipline. The Elves are in a perpetual state of war, struggling to stave off the monsters outside their borders. The magical Elves are the chosen of the Goddess of Light.</p>
<img src="img/story/races_ranger.jpg" style="float:left; padding-left:50px;"> <img src="img/story/races_archer.jpg" style="float:right; padding-right:50px;"></div>

<div class="tab-panel" id="Vail">
<h2>The Vail</h2>

<p>Intelligent and swift, cunning and spiritual, the Vail are the chosen of the Goddess of Darkness. One of the two races that are descended from the Dumianas, the Vail, while exotic and mysterious in appearance, are not the warlike people most assume them to be. Ferocious and treacherous only when necessary, the Vail seek to find a state of balance in their surroundings. Not possessing the strength of some of the other races, the Vail prefer to use stealth and the mystical arts in battle.</p>
<img src="img/story/races_assassin.jpg" style="float:left; padding-left:50px;"> <img src="img/story/races_pagan.jpg" style="float:right; padding-right:50px;"></div>

<div class="tab-panel" id="Nordein">
<h2>The Nordein (A.K.A. "The Deatheaters")</h2>

<p>Towards the end of the great war, the Goddesses created the Nordein to help protect Teos. The Nordein, or "Deatheaters," have every appearance of being brutish, violent, and aggressive; however, this race of ancients is a book that should not be judged by its cover. In the wars that followed their appearance on Teos, the Nordien acquired a disdain for magic and chose not to use lightly, if at all. This is likely one reason they sided with the spiritual Vail, and not the magical Elves.</p>
