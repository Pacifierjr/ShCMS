<h2>Map Overview</h2>

<p>To bring up your map press the "M" key on your keyboard or press the parchment button on the radar.</p>

<div class="image"><img src="img/Story/MappSS.jpg"></div>

<ol style="margin-left:5px !important;">
	<li><strong>Quests</strong><br>
	The black icons on your map are NPC quests. Go to these points and speak to an NPC with a floating icon above their head.</li>
	<li><strong>Completed Quests</strong><br>
	The blue icons are NPCs you need to visit after you’ve met all the requirements of a quest. These NPCs will give you your reward.</li>
	<li><strong>Portal Gates</strong><br>
	There are many portal icons in every region, but only the most important ones are displayed on your map. Portal Icons will show up as a yellow or orange star.</li>
	<li><strong>Potion Merchant</strong><br>
	The Potion Icon on the Map indicates that there is a potion vendor located there.</li>
	<li><strong>Warehouse Keeper</strong><br>
	The Bag Icon represents the location of Warehouse keepers. Warhouse keepers are used to store your loot drops from Quests and Monsters.</li>
	<li><strong>Blacksmith</strong><br>
	The Hammer Icon shows the location of Blacksmiths, these NPC's can assist you with Linking, Enchanting your items and provide additional options.</li>
	<li><strong>Gatekeeper</strong><br>
	Gatekeepers provide convenient travel throughout Teos. You can use their service provided you are the correct level and have enough Gold to pay for the service.</li>
</ol>
