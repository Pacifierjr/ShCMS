<h2>HUD/GUI Overview</h2>

<div class="image"><img src="img/Story/UIfinal.jpg"></div>

<ol style="margin-left:5px !important;">
	<li><strong>Character Status Window</strong><br>
	Displays Character name, level, current health (HP, current mana (MP), and stamina.</li>
	<li><strong>Target Page</strong><br>
	Displays vitals for targeted enemy or player (name, status, characteristic).</li>
	<li><strong>Buff/DeBuff Check</strong><br>
	Shows current buff (or de-buff) effect and the its remaining time.</li>
	<li><strong>Hotkey Bar (P)</strong><br>
	Emotes, basic behaviors, skills, magic, and consumable items may be dragged into the hotkey slots and activated using the numerical keys (1-0) on the top of your keyboard. Pressing the "+" button on the Hotkey Bar will add a second bar to the player's HUD (these slots are activated using the number-pad keys). Pressing the arrow button will toggle the Hotkey Bar between vertical and horizontal orientation.</li>
	<li><strong>Character Name</strong><br>
	The name of the player character. Identity code or rank may appear along the sides of the name while in a party or in player-versus-player (PvP) mode.</li>
	<li><strong>Radar (V)</strong><br>
	Indicates enemy Locations, NPCs, quests, and quest rewards. Clicking on the "+" or "-" will zoom in and out. Clicking on the parchment paper (M) will bring up a map of the entire realm.</li>
	<li><strong>Function Buttons</strong><br>
	<em>From left to right:</em> Shop, Status, Inventory, Assistant Skill, Quest, Community, Options.</li>
	<li><strong>Shop</strong><br>
	This is where players can spend Aeria Points (AP) to enhance their gameplay experience.</li>
	<li><strong>Level Bar</strong><br>
	The percentage of experience gained towards the next level.</li>
	<li><strong>Stats Screen</strong><br>
	The current status of your character including class, level, title, stats and other useful information.</li>
</ol>

